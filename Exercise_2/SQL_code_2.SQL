SELECT DEPARTMENT_ID, SUM(SALARY) AS SUM, AVG(SALARY) AS AVG
FROM EMPLOYEES, JOBS
WHERE MIN_SALARY >= 5000
GROUP BY DEPARTMENT_ID;

